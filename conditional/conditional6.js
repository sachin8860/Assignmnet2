let sum = 80 + 77 + 88 + 95 + 68;
let avg = sum/5;
console.log("Average Marks = " + avg);
if(avg<60){
    console.log("F Grade");
}
else if(avg<70){
    console.log("D Grade");
}
else if(avg<80){
    console.log("C Grade");
}
else if(avg<90){
    console.log("B Grade");
}
else if(avg<100){
    console.log("A Grade");
}